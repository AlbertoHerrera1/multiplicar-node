const opts = {
    base: {
        demand: true,
        alias: 'b'
    },
    limite: {
        alias: 'l',
        default: 10
    }
}


const argv = require('yargs')
    .command('listar', 'Imprime en consola la tabla de multiplicar', opts)
    .command('crear', 'Crear un archivo dado un factor con la tabla de multiplicar de dicho factor', opts)
    .help()
    .argv;

module.exports = {
    argv
}